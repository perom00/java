package edunova;

public class E02PrimitivniTipovi {

	public static void main(String[] args) {
		boolean aktivan = true;
		System.out.println(aktivan);
		
		
		byte malibroj=-24;
		System.out.println(malibroj);
		
		short malovecibroj=1322;
		System.out.println(malovecibroj);
		
		int cijelibroj=2000;
		System.out.println(cijelibroj);
		
		long dugackibroj=5598555;
		System.out.println(dugackibroj);
		
		float manjidecimalni=3.14f;
		System.out.println(manjidecimalni);
		
		double velikidecimalni=3.15435;
		System.out.println(velikidecimalni);
		
		// znakovni tip char mora biti jedno slovo ili broj
		
		char znak = '1';
		System.out.println(znak);

		
	}

}
