package edunova;

public class E03Omotaci {
	public static void main(String[] args) {
		int i = Integer.parseInt("26");
		System.out.println(i);

		i = Integer.MAX_VALUE;
		i = i + 1;

		System.out.println(i);

		float f = Float.parseFloat("3.14");
		System.out.println(f);

		char c = Character.MIN_VALUE;
		
		System.out.println(c);
		
	}

}
